	public void setAdmid(int admid) {
		this.admid = admid;
	}
	public String getAdmname() {
		return admname;
	}
	public void setAdmname(String admname) {
		this.admname = admname;
	}
	public String getAdmpass() {
		return admpass;
	}
	public void setAdmpass(String admpass) {
		this.admpass = admpass;
	}
